// Números

// Definição dos números
const n1 = 2;
const n2 = 5;

// Impressão dos números
console.log(n1); // Apenas imprime o número
console.log(n1, n2); // Imprime os números separados por espaço (não é concatenação real)

// Operações básicas
console.log(n1 + n2); // Soma os números

// Exemplos de cálculos

// Soma
const soma = n1 + n2;
console.log(`${n1} + ${n2} = ${soma}`);

// Subtração
const subtracao = n1 - n2;
console.log(`${n1} - ${n2} = ${subtracao}`);

// Divisão
const divisao = n1 / n2;
console.log(`${n1} ÷ ${n2} = ${divisao}`);

// Multiplicação
const multiplicacao = n1 * n2;
console.log(`${n1} × ${n2} = ${multiplicacao}`);



